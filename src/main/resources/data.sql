DROP TABLE IF EXISTS ITEMS;
DROP TABLE IF EXISTS INVOICES;

CREATE TABLE ITEMS (
  id INT AUTO_INCREMENT  PRIMARY KEY,
  NAME varchar2(255) NOT NULL,
  AMOUNT DECIMAL(10) NOT NULL,
  INVOICE_ID INT
);

CREATE TABLE INVOICES (
  id INT AUTO_INCREMENT  PRIMARY KEY,
  CREATION_DATE DATE NOT NULL
);

